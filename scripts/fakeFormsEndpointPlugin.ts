const sendJsonResponse = (
  res: {
    statusCode: number;
    setHeader: (name: string, value: string) => void;
    end: (body?: string) => void;
  },
  statusCode: number,
  body: unknown,
) => {
  res.statusCode = statusCode;
  Object.entries(jsonHeaders).forEach(([name, value]) => res.setHeader(name, value));
  res.end(JSON.stringify(body));
};

const createFakeFormsMiddleware = (): Middleware => {
  return (req, res, next) => {
    const method = req.method ?? "GET";
    const rawUrl = req.url ?? "";
    const requestUrl = new URL(rawUrl, "http://localhost");

    if (requestUrl.pathname !== FAKE_FORMS_ENDPOINT_PATH) {
      next();
      return;
    }

    if (method === "GET") {
      const requestedFormCode = requestUrl.searchParams.get("form") ?? "ContactForm";
      const response = createFormRenderingData(requestedFormCode);

      sendJsonResponse(res, 200, response);
      return;
    }

    if (method === "POST") {
      const shouldFail = requestUrl.searchParams.get("mockSubmitError") === "1";

      if (shouldFail) {
        sendJsonResponse(res, 422, {
          data: {
            message: "Validation failed",
            errors: {
              email: "Email is invalid",
            },
          },
        });
        return;
      }

      sendJsonResponse(res, 200, { ok: true });
      return;
    }

    sendJsonResponse(res, 405, { message: "Method not allowed" });
  };
};

export const fakeFormsEndpointPlugin = (): Plugin => {
  const middleware = createFakeFormsMiddleware();

  return {
    name: "fake-forms-endpoint",
    configureServer(server) {
      server.middlewares.use(middleware);
    },
    configurePreviewServer(server) {
      server.middlewares.use(middleware);
    },
  };
};

